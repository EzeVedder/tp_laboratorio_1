int sumar(int *A, int *B,int *pSuma)
{

    *pSuma = *A+*B;

    return 0;
}


int restar(int *A, int *B,int *pResta)
{

    *pResta = *A-*B;

    return 0;
}

int dividir(int *A, int *B,int *pDivision)
{

    *pDivision = *A/ *B;

    return 0;
}

int multiplicar(int *A, int *B,int *pMultiplicar)
{
    *pMultiplicar = *A* *B;

    return 0;
}
